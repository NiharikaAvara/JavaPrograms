class PrimitiveDataTypes
{
static byte b;
static int i;
static short s;
static long l;
static float f;
static double d;
public static void main(String[] args)
{
System.out.println(b);
System.out.println(i);
System.out.println(s);
System.out.println(l);
System.out.println(f);
System.out.println(d);
}
}
